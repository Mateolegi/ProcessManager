package com.mateolegi.despliegues_audiencias.util;

public class VersionResponse {

    private int despliegue;
    private String fullVersion;
    private String stable;
    private String version;

    public int getDespliegue() { return despliegue; }
    public void setDespliegue(int despliegue) { this.despliegue = despliegue; }
    public String getFullVersion() { return fullVersion; }
    public void setFullVersion(String fullVersion) { this.fullVersion = fullVersion; }
    public String getStable() { return stable; }
    public void setStable(String stable) { this.stable = stable; }
    public String getVersion() { return version; }
    public void setVersion(String version) { this.version = version; }
}
